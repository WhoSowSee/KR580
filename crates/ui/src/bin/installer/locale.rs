impl Locale {
    pub fn system() -> Self {
        match k580_ui::system_locale::default_language() {
            k580_ui::persistence::Language::Ru => Self::Ru,
            k580_ui::persistence::Language::En => Self::En,
        }
    }

    pub fn t(self, text: Text) -> &'static str {
        match self {
            Self::Ru => ru(text),
            Self::En => en(text),
        }
    }
}

