pub fn glyph(code: u8) -> [u8; GLYPH_HEIGHT] {
    if code == 0 {
        return [0; GLYPH_HEIGHT];
    }
    for (c, rows) in TABLE.iter() {
        if *c == code {
            return *rows;
        }
    }
    FALLBACK
}

pub fn pixel_lit(code: u8, col: usize, row: usize) -> bool {
    if col >= GLYPH_WIDTH || row >= GLYPH_HEIGHT {
        return false;
    }
    let rows = glyph(code);
    let bit = (GLYPH_WIDTH - 1) - col;
    (rows[row] >> bit) & 1 == 1
}
